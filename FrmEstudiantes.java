    static void buscarJugador() {
        int id = Integer.parseInt(txtId.getText());
        for (Jugadores jugador : jugadores) {
            if (jugador.getId() == id) {
                txtNombre.setText(jugador.getNombre());
                txtJuego.setText(jugador.getJuegoFavortio());
                txtScore.setText(String.valueOf(jugador.getMaxScore()));
                txtLevel.setText(Character.toString(jugador.getLevel()));
            }
        }
        listarJugadores();
    }
    static void eliminarJugador() {
        int id = Integer.parseInt(txtId.getText());
        for (Jugadores jugador : jugadores) {
            if (jugador.getId() == id) {
                jugadores.remove(jugador);
                //OptionPane.showInternalMessageDialog("Se ha eliminado el jugador ");
                System.out.println("Se ha eliminado el jugador " + jugador.getId());    
            }
        }
        listarJugadores();
    }
    static void editarJugador(){
        int id = Integer.parseInt(txtId.getText());
        for (Jugadores jugador : jugadores) {
            if (jugador.getId() == id) {
                jugador.setNombre(txtNombre.getText());
                jugador.setJuegoFavortio(txtJuego.getText());
                jugador.setLevel(txtLevel.getText().charAt(0));
                jugador.setMaxScore(Integer.parseInt(txtScore.getText()));    
                
                System.out.println("Se ha editado el jugador " + jugador.getId());  
            }
        }
        listarJugadores();
    }
    
    static void guardarTxt(){
        File archivo = new File("jugadores.txt");
        try {
            archivo.createNewFile();
            FileWriter fw = new FileWriter(archivo);
            String jugadoresTxt = "";
            
            for (Jugadores jugador : jugadores){
                jugadoresTxt += jugador.getNombre()+", "+jugador.getJuegoFavortio()+", "+jugador.getLevel()+", "+jugador.getMaxScore()+"\n";
            }
            fw.write(jugadoresTxt);
            fw.close();
            System.out.println(archivo.getAbsolutePath());
           
        } // fin try 
        catch (IOException ex) {
            Logger.getLogger(FrmEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
        } // fin catch
    }
